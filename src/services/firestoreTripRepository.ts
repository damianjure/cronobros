import {
  collection,
  doc,
  deleteDoc,
  getDoc,
  getDocs,
  onSnapshot,
  runTransaction,
  setDoc,
  writeBatch,
  type CollectionReference,
  type DocumentData,
  type Firestore,
} from 'firebase/firestore';
import type {
  ItineraryDay,
  ItineraryActivity,
  PinnedPoint,
  PendingPlace,
  ChatMessage,
  TripLogistics,
  CriticalEvent,
} from '../types';
import { mapCategoryToActivityType } from '../utils/category';
import { db as defaultDb } from '../lib/firebase';
import type { TripRepository, Unsubscribe } from './ports';

